const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
const isTouch = window.matchMedia("(pointer: coarse)").matches;
const isSmallScreen = window.matchMedia("(max-width: 980px)").matches;
const TURBO_MOTION = false;
const motionPreset = isTouch || isSmallScreen
  ? {
      heroScrub: 0.35,
      sectionY: 10,
      sectionDur: 0.46,
      sectionStagger: 0.055,
      storyScrub: 0.55,
      storyStep: 440,
      storySnapMin: 0.16,
      storySnapMax: 0.3
    }
  : {
      heroScrub: 0.55,
      sectionY: 14,
      sectionDur: 0.52,
      sectionStagger: 0.07,
      storyScrub: 0.85,
      storyStep: 520,
      storySnapMin: 0.24,
      storySnapMax: 0.44
    };

const tunedMotion = TURBO_MOTION
  ? {
      ...motionPreset,
      heroScrub: Math.max(0.2, motionPreset.heroScrub - 0.15),
      sectionY: Math.max(6, motionPreset.sectionY - 4),
      sectionDur: Math.max(0.3, motionPreset.sectionDur - 0.14),
      sectionStagger: Math.max(0.03, motionPreset.sectionStagger - 0.02),
      storyScrub: Math.max(0.3, motionPreset.storyScrub - 0.25),
      storyStep: Math.max(340, motionPreset.storyStep - 120),
      storySnapMin: 0.12,
      storySnapMax: 0.24
    }
  : motionPreset;

let smoothScroller = null;
if (window.Lenis && !reducedMotion && !isTouch) {
  const lenis = new Lenis({
    lerp: TURBO_MOTION ? 0.14 : 0.09,
    wheelMultiplier: TURBO_MOTION ? 0.9 : 0.82,
    smoothWheel: true,
    syncTouch: false
  });
  smoothScroller = lenis;

  lenis.on("scroll", () => {
    if (window.ScrollTrigger) ScrollTrigger.update();
  });

  if (window.gsap) {
    gsap.ticker.add((time) => {
      lenis.raf(time * 1000);
    });
    gsap.ticker.lagSmoothing(0);
  }
}

document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener("click", (event) => {
    const targetId = anchor.getAttribute("href");
    if (!targetId || targetId === "#") return;
    const target = document.querySelector(targetId);
    if (!target) return;

    if (smoothScroller) {
      event.preventDefault();
      smoothScroller.scrollTo(target, {
        duration: TURBO_MOTION ? 0.75 : 1.05,
        easing: (t) => 1 - Math.pow(1 - t, 3)
      });
    }
  });
});

if (!reducedMotion) {
  window.addEventListener("scroll", () => {
    topbar.classList.toggle("is-scrolled", window.scrollY > 12);
  });
}

if (window.gsap && !reducedMotion) {
  gsap.registerPlugin(ScrollTrigger);
  gsap.config({ force3D: true });
  gsap.defaults({ ease: "power2.out", overwrite: "auto" });

  gsap.timeline()
    .from(".eyebrow", { opacity: 0, y: 12, duration: 0.5 })
    .from(".anim-hero", { opacity: 0, y: 20, duration: 0.62, stagger: 0.08 }, "-=0.14");

  gsap.to(".hero-media img", {
    yPercent: 6,
    ease: "none",
    scrollTrigger: {
      trigger: "#inicio",
      start: "top top",
      end: "bottom top",
      scrub: tunedMotion.heroScrub
    }
  });

  gsap.from(".project-card", {
    scrollTrigger: { trigger: "#trabalhos", start: "top 76%" },
    opacity: 0,
    y: tunedMotion.sectionY + 4,
    duration: tunedMotion.sectionDur + 0.06,
    stagger: tunedMotion.sectionStagger + 0.015
  });

  gsap.from(".service-list li", {
    scrollTrigger: { trigger: "#servicos", start: "top 78%" },
    opacity: 0,
    y: tunedMotion.sectionY - 2,
    duration: tunedMotion.sectionDur - 0.02,
    stagger: tunedMotion.sectionStagger - 0.01
  });

  gsap.from(".process-item, .quote", {
    scrollTrigger: { trigger: "#processo", start: "top 80%" },
    opacity: 0,
    y: tunedMotion.sectionY,
    duration: tunedMotion.sectionDur,
    stagger: tunedMotion.sectionStagger
  });

  gsap.from(".contact-links a", {
    scrollTrigger: { trigger: "#contato", start: "top 88%" },
    opacity: 0,
    y: 8,
    duration: 0.42,
    stagger: 0.05
  });

  const slides = gsap.utils.toArray(".story-slide");
  const enablePinnedStory =
    slides.length && window.matchMedia("(min-width: 981px)").matches && !isTouch && !TURBO_MOTION;
  if (enablePinnedStory) {
    gsap.set(slides[0], { opacity: 1, y: 0, scale: 1 });
    gsap.set(".story-slide h3, .story-slide p, .story-kicker", { opacity: 0, y: 18 });
    gsap.set(slides[0].querySelectorAll(".story-kicker, h3, p"), { opacity: 1, y: 0 });

    slides.forEach((slide) => {
      gsap.to(slide, {
        backgroundPosition: "50% 54%",
        ease: "none",
        scrollTrigger: {
          trigger: "#narrativa",
          start: "top top",
          end: "bottom top",
          scrub: tunedMotion.storyScrub
        }
      });
    });

    const storyTl = gsap.timeline({
      scrollTrigger: {
        trigger: "#narrativa",
        start: "top top",
        end: `+=${slides.length * tunedMotion.storyStep}`,
        scrub: tunedMotion.storyScrub,
        pin: true,
        snap: {
          snapTo: 1 / (slides.length - 1),
          duration: { min: tunedMotion.storySnapMin, max: tunedMotion.storySnapMax },
          delay: 0.02,
          ease: "power1.inOut"
        }
      }
    });

    slides.forEach((slide, i) => {
      if (i === 0) return;
      const prevInner = slides[i - 1].querySelectorAll(".story-kicker, h3, p");
      const nextInner = slide.querySelectorAll(".story-kicker, h3, p");

      storyTl.to(prevInner, { opacity: 0, y: -12, stagger: 0.028, duration: 0.24 }, i - 0.28);
      storyTl.to(slides[i - 1], { opacity: 0, y: -18, scale: 0.988, duration: 0.36 }, i - 0.2);
      storyTl.fromTo(
        slide,
        { opacity: 0, y: 28, scale: 0.985 },
        { opacity: 1, y: 0, scale: 1, duration: 0.56 },
        i - 0.08
      );
      storyTl.fromTo(nextInner, { opacity: 0, y: 14 }, { opacity: 1, y: 0, stagger: 0.048, duration: 0.34 }, i);
    });
  } else {
    gsap.set(".story-slide", { clearProps: "opacity,transform,filter" });
    gsap.set(".story-slide", { opacity: 1, y: 0, scale: 1 });
    gsap.set(".story-slide h3, .story-slide p, .story-kicker", { opacity: 1, y: 0 });
  }
}

